package entity;

/**
 *
 * @author Tran Trang
 */
public class User {
    private int id;
    private String name;
    private boolean gender;
    private String password;
    private int roleID;

    public User() {
    }

    public User(int id, String name, boolean gender, String password, int roleID) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.password = password;
        this.roleID = roleID;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getRoleID() {
        return roleID;
    }

    public void setRoleID(int roleID) {
        this.roleID = roleID;
    }
    
}
